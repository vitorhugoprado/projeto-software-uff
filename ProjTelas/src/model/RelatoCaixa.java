package model;
public class RelatoCaixa {

    int codV;
    String pag;
    float totem;
    
    public int getCodV() {
        return codV;
    }

    public void setCodV(int codV) {
        this.codV = codV;
    }

    public String getPag() {
        return pag;
    }

    public void setPag(String pag) {
        this.pag = pag;
    }

    public float getTotem() {
        return totem;
    }

    public void setTotem(float totem) {
        this.totem = totem;
    }
     
}
